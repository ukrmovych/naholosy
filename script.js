    questionEl.textContent = "–Ω–µ–∑–∞–±–∞—Ä–æ–º —Ç—É—Ç –∑'—è–≤–∏—Ç—å—Å—è –∫–∞–π—Ñ—É–∂–Ω–∏–π —Ç—Ä–µ–Ω–∞–∂–µ—Ä, –Ω–µ –ø—Ä–æ–ø—É—Å—Ç–∏! üòäüòä";
